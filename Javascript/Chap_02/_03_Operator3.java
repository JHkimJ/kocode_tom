package Chap_02;

public class _03_Operator3 {
    public static void main(String[] args) {
        //비교 연산자
        System.out.println(5 > 3); // 5 는 3보다 크다. True
        System.out.println(5 < 3); // 5 는 3보다 작다. False
        System.out.println(5 >= 3); // 5 는 3보다 크거나 같다. True
        System.out.println(5 <= 3); // 5 는 3보다 작거나 같다. False

        System.out.println(5 == 5); // 5 는 5 와 같다 True
        System.out.println(5 == 3); // 5 는 3 과 같다 False
        System.out.println(5 != 5); // 5 는 5와 같지않다 False
        System.out.println(5 != 3); // 5 는 3과 같지않다 True
    }
}
